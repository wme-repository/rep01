import { loadConfig } from '../config/index.js';
import { logger } from '../services/logger.js';

const ARVO_API_URL = process.env.ARVO_API_URL || 'https://api.arvo.ai/v1/generate';

const DEFAULT_RETRYABLE = ['ECONNRESET', 'ETIMEDOUT', 'ENOTFOUND', '429', '503', '504'];

async function withRetry(fn, options = {}) {
  const { maxRetries = 3, initialDelay = 1000, maxDelay = 30000, backoffFactor = 2 } = options;
  let lastError;
  let delay = initialDelay;

  for (let attempt = 1; attempt <= maxRetries + 1; attempt++) {
    try {
      const result = await fn();
      if (attempt > 1) logger.info(`Retry succeeded on attempt ${attempt}`);
      return { result, attempts: attempt, finalError: null };
    } catch (err) {
      lastError = err;
      const isRetryable = DEFAULT_RETRYABLE.some(e =>
        err.message?.includes(e) || err.code === e || err.status === e
      );
      if (attempt > maxRetries + 1 || !isRetryable) {
        logger.error(`Non-retryable error after ${attempt} attempts: ${err.message}`);
        return { result: null, attempts: attempt, finalError: err };
      }
      logger.warn(`Attempt ${attempt} failed: ${err.message}. Retrying in ${delay}ms...`);
      await new Promise(r => setTimeout(r, delay));
      delay = Math.min(delay * backoffFactor, maxDelay);
    }
  }
  return { result: null, attempts: maxRetries + 1, finalError: lastError };
}

export async function generateArticle(prompt, options = {}) {
  const config = loadConfig();
  const { articleId, title, outline, keyword, funnelStage, metaDescription } = options;

  const dryRun = options.dryRun || process.env.DRY_RUN === 'true';

  // Dry run doesn't need API key
  if (dryRun) {
    const systemPrompt = buildSystemPrompt(keyword, funnelStage);
    const userPrompt = buildUserPrompt(title, outline, keyword);
    logger.info('[arvo] DRY RUN - would send to API');
    return {
      article: `## DRY RUN ARTICLE\n\n**Title:** ${title}\n**Keyword:** ${keyword}\n**Funnel Stage:** ${funnelStage || 'awareness'}\n\n---\n\n${outline ? `### Outline:\n${outline.map((s, i) => `${i + 1}. **${s.heading}**: ${s.description || ''}`).join('\n')}\n\n---\n\n` : ''}### Article Content\n\n[This would be generated by Arvo API with the following prompt:]\n\n**System:** ${systemPrompt}\n\n**User:** ${userPrompt}`,

      title,
      metaDescription: metaDescription || '',
      articleId,
      dryRun: true
    };
  }

  const apiKey = config.ARVO_API_KEY;
  if (!apiKey) {
    throw new Error('ARVO_API_KEY not configured. Set ARVO_API_KEY in .env');
  }

  const systemPrompt = buildSystemPrompt(keyword, funnelStage);
  const userPrompt = buildUserPrompt(title, outline, keyword);

  try {
    const { result } = await withRetry(async () => {
      const response = await fetch(ARVO_API_URL, {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${apiKey}`,
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({
          system_prompt: systemPrompt,
          user_prompt: userPrompt,
          max_tokens: options.maxTokens || 2000,
          temperature: options.temperature || 0.7
        })
      });

      if (!response.ok) {
        const err = await response.text();
        throw new Error(`Arvo API error: ${response.status} - ${err}`);
      }

      return response.json();
    });

    if (!result) throw new Error('Arvo API returned no result');

    const article = result.article || result.content || result.text || JSON.stringify(result);
    const generatedTitle = result.title || title;
    const generatedMeta = result.meta_description || result.metaDescription || metaDescription || '';

    logger.info(`[arvo] Generated article: ${generatedTitle}`);

    return {
      article,
      title: generatedTitle,
      metaDescription: generatedMeta,
      articleId
    };
  } catch (err) {
    logger.error(`[arvo] Generation failed: ${err.message}`);
    throw err;
  }
}

function buildSystemPrompt(keyword, funnelStage) {
  return `You are an expert SEO content writer for a Brazilian Portuguese website.

Keyword focus: "${keyword}"
Funnel stage: ${funnelStage || 'awareness'}

Rules:
- Write in Brazilian Portuguese
- Use primary keyword in first 100 words
- Include keyword in title, H1, and at least 2 H2 subheadings
- Meta description: 140-160 characters with keyword
- Article length: 800-1500 words
- Use semi-formal tone, engaging but professional
- Include CTA at end matching the funnel stage
- No keyword stuffing - natural language only
- Structure: intro, body with H2/H3, conclusion with CTA`;
}

function buildUserPrompt(title, outline, keyword) {
  if (!outline) {
    return `Write a complete SEO article about "${keyword}" with title "${title}". Follow all rules from system prompt.`;
  }

  const outlineText = outline.map((s, i) =>
    `${i + 1}. ${s.heading}${s.description ? `: ${s.description}` : ''}`
  ).join('\n');

  return `Generate a complete SEO article based on this brief:

TITLE: ${title}
KEYWORD: ${keyword}
OUTLINE:
${outlineText}

Write the full article following the outline. Include an engaging introduction, substantive body sections with examples where appropriate, and a conclusion with a call-to-action.`;
}

export async function generateArticleFromCalendar(calendarEntry, options = {}) {
  return generateArticle(null, {
    articleId: calendarEntry.id,
    title: calendarEntry.title,
    outline: calendarEntry.outline,
    keyword: calendarEntry.keyword,
    funnelStage: calendarEntry.funnelStage,
    metaDescription: calendarEntry.metaDescription,
    ...options
  });
}
